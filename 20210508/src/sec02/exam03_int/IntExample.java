package sec02.exam03_int;

public class IntExample {
	public static void main(String[] args) {
		int var1 = 10;
		int var2 = 012; 					// 8진수
		int var3 = 0xA;						// 16진수
		
		int var4 = 1000000000;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
		System.out.println(var4);
	}
}
